public:
  httpResponse post(String requestPage, String postContent) {
    //save http code and body
    int codeHTTP = 0;
    String bodyHTTP = "";

    //make new secure client
    WiFiClientSecure *sec_client = new WiFiClientSecure;
    if (sec_client) {
      //set root certificate
      sec_client->setCACert(rootCertificate);
      {
        //make new http client
        HTTPClient httpClients;

        //collect the cookie header
        httpClients.collectHeaders(collectedHeaders, 1);
        //connect to the requested page
        if (httpClients.begin(*sec_client, _baseURL + requestPage)) {
          // The data to post is "application/x-www-form-urlencoded"
          httpClients.addHeader("Content-Type", "application/x-www-form-urlencoded");

          // Set the cookie if not already set 
          if (_cookie != "") httpClients.addHeader("Cookie", _cookie);
          //post to requested page and save http code
          codeHTTP = httpClients.POST(postContent);

          // Get the new cookie
          if (httpClients.hasHeader("Set-Cookie")) _cookie = httpClients.header("Set-Cookie");
          //save the body of the response
          bodyHTTP = httpClients.getString();
        }
        httpClients.end();
      }
      //frees up resources
      delete sec_client;
    }

    // Return the struct which includes the http code and the json response
    httpResponse returnVal = { codeHTTP, JSON.parse(bodyHTTP) };
    return returnVal;
  }
